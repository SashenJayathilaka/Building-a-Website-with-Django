import turtle

get_Turtle = turtle.Turtle()

def square():
    get_Turtle.forward(100)
    get_Turtle.right(90)
    get_Turtle.forward(100)
    get_Turtle.right(90)
    get_Turtle.forward(100)
    get_Turtle.right(90)
    get_Turtle.forward(100)

quiz = "Happy"

while quiz == "Happy":
    square()


