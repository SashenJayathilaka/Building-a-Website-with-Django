import turtle

get_Turtle = turtle.Turtle()

def square():
    get_Turtle.forward(100)
    get_Turtle.right(90)
    get_Turtle.forward(100)
    get_Turtle.right(90)
    get_Turtle.forward(100)
    get_Turtle.right(90)
    get_Turtle.forward(100)

square()
get_Turtle.forward(200)
square()
get_Turtle.forward(200)
square()
get_Turtle.forward(200)
square()
get_Turtle.forward(200)
